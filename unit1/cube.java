public class Cubes {
    public static void main(String[] args) {
        // Loop to print cubes of counting numbers from 0 to 10 (or more if needed)
        for (int i = 0; i <= 10; i++) {
            int cube = i * i * i; // Calculate cube of i
            System.out.println("Cube of " + i + " is: " + cube);
        }
    }
}
